public class Notes {
    public static void main(String[] args) {
        System.out.println(6 & 1111); // both is true gives 1
        System.out.println(6 ^ 1111); // if and only if 1 is true gives 1
        System.out.println(6 | 1111); // either this or that is true gives 1
        System.out.println(6 & 1);
        System.out.println(6 ^ 1);
        System.out.println(6 | 1);
    }
}
